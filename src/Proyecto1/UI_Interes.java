    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        for (int i = 0; i < 4; i++) {
            if (i!=variable) {
                Simple.SetVariable(i, var[i]);
            }
        }
        
        if (Tipo.getSelectedIndex()==0) {
            
            switch(variable){
                case 0:
                    aux=Simple.CalculaVP();
                    var[variable]=aux;
                    Simple.SetVariable(variable, aux);
                    VP.setText(decimales.format(aux));
                    break;
                case 1:
                    aux=Simple.CalculaVF();
                    var[variable]=aux;
                    Simple.SetVariable(variable, aux);
                    VF.setText(decimales.format(aux));
                    break;
                case 2:
                    aux=Simple.CalculaI();
                    var[variable]=aux;
                    Simple.SetVariable(variable, aux);
                    I.setText(decimales.format(aux));
                    break;
                default:
                    aux=Simple.CalculaN();
                    var[variable]=aux;
                    Simple.SetVariable(variable, aux);
                    N.setText(decimales.format(aux));
                    break;
            }
            
        }
        else{
            switch(variable){
                case 0:
                    aux=Compuesto.CalculaVP();
                    aux=Math.round(aux*100000000d)/100000000d;
                    var[variable]=aux;
                    Compuesto.SetVariable(variable, aux);
                    VP.setText(aux+"");
                    break;
                case 1:
                    aux=Compuesto.CalculaVF();
                    aux=Math.round(aux*100000000d)/100000000d;
                    var[variable]=aux;
                    Compuesto.SetVariable(variable, aux);
                    VF.setText(aux+"");
                    break;
                case 2:
                    aux=Compuesto.CalculaI();
                    aux=Math.round(aux*100000000d)/100000000d;
                    var[variable]=aux;
                    Compuesto.SetVariable(variable, aux);
                    I.setText(aux+"");
                    break;
                default:
                    aux=Compuesto.CalculaN();
                    aux=Math.round(aux*100000000d)/100000000d;
                    var[variable]=aux;
                    Compuesto.SetVariable(variable, aux);
                    N.setText(aux+"");
                    break;
            }
        }
    }//GEN-LAST:event_CalcularActionPerformed
    
    private void ValidaNumero(java.awt.event.KeyEvent evt) {                            
        char caracter = evt.getKeyChar();
        //                   Numero                            Decimal        Negativo             Borrar
        if(((caracter < '0') || (caracter > '9')) && (caracter != '.') && (caracter != '-') && (caracter != '\b') ){
            evt.consume();  // ignorar el evento de teclado
        }
    }
    
    private void ValidaParametros(){
        
        int contador=0;
        
        //valor presente
        if (!VP.getText().isEmpty()) {
            try{
                aux=parseFloat(VP.getText());
                aux=Math.round(aux*100000000d)/100000000d;
                var[0]=aux;
                VP.setText(aux+"");
            }
            catch(Exception e){
                contador++;
            }
            
        }
        else{
            contador++;
            variable=0;
        }
        
        //valor futuro
        if (!VF.getText().isEmpty()) {
            try{
                aux=parseFloat(VF.getText());
                aux=Math.round(aux*100000000d)/100000000d;
                var[1]=aux;
                VF.setText(aux+"");
            }
            catch(Exception e){
                contador++;
            }
            
        }
        else{
            contador++;
            variable=1;
        }
        
        //Interes
        if (!I.getText().isEmpty()) {
            try{
                aux=parseFloat(I.getText());
                aux=Math.round(aux*100000000d)/100000000d;
                var[2]=aux;
                I.setText(aux+"");
            }
            catch(Exception e){
                contador++;
            }
            
        }
        else{
            contador++;
            variable=2;
        }
        
        //Numero de periodos
        if (!N.getText().isEmpty()) {
            try{
                aux=parseFloat(N.getText());
                aux=Math.round(aux*100000000d)/100000000d;
                var[3]=aux;
                N.setText(aux+"");
            }
            catch(Exception e){
                contador++;
            }
            
        }
        else{
            contador++;
            variable=3;
        }
        
       
        
        
        //validamos si habilitar el boton calcular
        if (contador == 1) {
            Calcular.setEnabled(true);
        }
        else{
            Calcular.setEnabled(false);
        }
    }
